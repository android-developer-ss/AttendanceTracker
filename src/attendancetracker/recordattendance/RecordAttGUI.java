    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
        
        CourseDomainLayer courseProc = new CourseDomainLayer();
        CourseStruct courseArray[] = courseProc.getCourseList();
        CourseStruct course;
        for(int i= 0; i<CourseDomainLayer.numOfCourses;i++){
            //course = new CourseStruct();
            course = courseArray[i];
            courseIDCombo.addItem(course.courseID);
        }
        
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        String dateStr;
        dateStr = dateFormat.format(date);
        
        dateYearCombo.setSelectedItem(dateStr.substring(0, 4));
        dateMonthCombo.setSelectedItem(dateStr.substring(5, 7));
        dateDayCombo.setSelectedItem(dateStr.substring(8,10));
        
        //studAttTable.setVisible(false);
        
    }//GEN-LAST:event_formWindowActivated

    
    
