    private void getAttendanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getAttendanceActionPerformed
        studAttTable.setVisible(true);
        
        
         // Clear the Table which is used to display the students which are 
        // registered for the selected course.
         for(int row = 0; row<60; row++){
            for(int col = 0 ; col<=4; col++){
                studAttTable.setValueAt(null, row, col);
            }
        }
         //Check if file exists for current CourseStruct and current given date.
          RecAttStruct recAtt = new RecAttStruct();
          recAtt.courseID = (String)courseIDCombo.getSelectedItem();
          recAtt.day      = (String)dateDayCombo.getSelectedItem();
          recAtt.month    = (String)dateMonthCombo.getSelectedItem();
          recAtt.year     = (String)dateYearCombo.getSelectedItem();
         Object[][] data;
         // Fill the table with the students who are registered for the selected 
         // course.
          //Object[][] data = recAttProc.getStudentsEnrolledForCourse(courseIDCombo.getSelectedItem().toString());
        data = recAttProc.checkIfAttendanceMarked(recAtt);
        
          for(int row = 0; row<CourseRegTechSerLayer.courseNumForCourse; row++){
            for(int col = 0 ; col<=4; col++){
                studAttTable.setValueAt(data[row][col], row, col);
            }
        }
          if(RecAttDomainLayer.fileFound==1)
              statusBar.setText("Attendace already recorded for this course"
                      + " and the date. Please make required changes in the"
                      + "attendance and SAVE.");
          else
              statusBar.setText("Please uncheck the boxes for Absent students"
                                + " and SAVE.");
    }//GEN-LAST:event_getAttendanceActionPerformed

